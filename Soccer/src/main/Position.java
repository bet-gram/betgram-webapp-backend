package main;

public class Position {
	
	/**
	 * name
	 */
	private String name;

	/**
	 * @param name
	 */
	public Position(String name) {
		this.name = name;
	}

	/**
	 * @return the name
	 */
	public String getName() {
		return name;
	}

	/**
	 * @param name the name to set
	 */
	public void setName(String name) {
		this.name = name;
	}

}
